get_config(Gamemode, Size, Board):-
        write('\33\[2J'),nl,
        write('SELECT GAME MODE'),nl,
        write('1) Player VS Player'),nl,
        write('2) Player VS AI'),nl,
        write('3) AI VS AI'),nl,
        read(Gamemode),
        
        write('\33\[2J'),nl,
        write('SELECT BOARD SIZE'),nl,
        write('1) 6x6'),nl,
        write('2) 8x8'),nl,
        write('3) 10x10'),nl,
        read(Size),

        load_board(Size, Board).

